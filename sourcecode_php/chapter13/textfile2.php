<?php 
	$file 	= "data.txt";  
    $fp 	= fopen($file, "r") or die("ไม่สามารถเปิดไฟล์ $file ได้");  
	// เปิดไฟล์ข้อมูลเพื่อทำการอ่านอย่างเดียว เลือกโหมด r    และทำการเช็คว่าเปิดไฟล์สำเร็จหรือไม่ด้วยฟังก์ชั่น or die()
    while (!feof($fp)) // ทำการวนลูปเพื่ออ่านข้อมูลจนสิ้นสุดไฟล์
	{  
         	$tmp_char = fgetc($fp);  // อ่านข้อมูลออกมาทีละตัวอักษร
	   		echo $tmp_char."<br>";  // โชว์ข้อมูลที่ได้จากการอ่านออกที่หน้าจอ และ ขึ้นบรรทัดใหม่
    }
    fclose($fp);  // ปิดไฟล์ข้อมูล 
?>
